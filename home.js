const exch = "https://cdn.jsdelivr.net/gh/fawazahmed0/currency-api@1/latest/currencies/"

    let dropdowns=document.querySelectorAll(" select");
    let btn=document.querySelector("form button");
    const fromcu=document.querySelector(".from select");
    const tocu=document.querySelector(".to select");
    const msg=document.querySelector(".msg");
    for(let sl of dropdowns){
        for(let cc in countryList){
        let op=document.createElement("option");
        op.innerText=cc;
        op.value=cc;
        if(sl.name==="from" && cc==="USD"){
            op.selected="selected";
        }else if(sl.name==="to" && cc==="INR"){
            op="selected";
        }
        sl.append(op);
        }
        sl.addEventListener("change",(e)=>{
            updateFlag(e.target);
        });
    }
    const updateFlag=(ele)=>{
        let cc=ele.value;
        let cf=countryList[cc];
        let sr=`https://flagsapi.com/${cf}/flat/64.png`;
        let im=ele.parentElement.querySelector("img");
        im.src=sr;

    }
    btn.addEventListener("click",async (ev)=>{
        ev.preventDefault();
        let amount=document.querySelector(".amount input");
        let amntv=amount.value;
        if(amntv===""||amntv<1){
            amntv=1;
            amount.value="1";
        }
        const url=`${exch}/${fromcu.value.toLowerCase()}/${tocu.value.toLowerCase()}.json`;
    let res=await fetch(url);
    let data= await res.json();
    let rate=data[tocu.value.toLowerCase()];
    let famo=amntv*rate;
    msg.innerHTML=`${amntv}${fromcu.value}=${famo} ${tocu.value}`;
    });
    